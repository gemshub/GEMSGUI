void TFGLcalc::BREED(float P1, float P2,float *GTP,float *DH )
{
    float KT,KP,KV,TR,PR1,PR2,x1,x2,RG=1.98719;
    KT=fg.KT;
    KP=fg.KP;
    KV=fg.KV;

    if(KT>=5.1&&KT<=5.28 || KT>=33.&&KT<=33.4 || KT>=44.&&KT<=44.8)
    {
        if(KT>=5.1&&KT<=5.28)
        {
            KT=10.47;
            KP=6.67; /*KV= 37.5;*/
        }
        if(KT>=33.&&KT<=33.4)
        {
            KT=43.6;
            KP=20.2; /*KV=51.5;*/
        }
        if(KT>=44.&&KT<=44.8)
        {
            KT=45.5;
            KP=26.9; /*KV=40.3;*/
        }
        KT=KT/(1.+21.8/(fg.MM*fg.TK));
        KP=KP/(1.+44.2/(fg.MM*fg.TK));
        KV=(0.292*83.144*KT)/KP;
    }
    if(KT>=647.&&KT<=647.6)
    {
        KT=480.81+157863.*(1./fg.TK);
        KP=251.95+53171.78*(1./fg.TK);
    }
    if(KT>=405.&&KT<=406.2)
    {
        KT=389.64+16326.99*(1./fg.TK);
        KP=118.13+16406.4*(1./fg.TK);
    }
    TR=fg.TK/KT;
    PR1=P1/KP;
    PR2=P2/KP;
    if(fg.Z)
    {
        fg.VLK=KV/densi1(TR,PR2);
        fg.Z=0;
    }
    x2=4.575673*fg.TK*(lfug(TR,PR2)+0.4342945*log(P2));
    if(P1>1.000000000001)
        x1=4.575673*fg.TK*(lfug(TR,PR1)+0.4342945*log(P1));
    else x1=0.;
    *GTP=x2-x1;
    fg.FUG=2.302585*lfug(TR,PR2);
    x2=-RG*KT*ENTALPIA(TR,PR2);
    if(P1>1.000000000001)
        x1=-RG*KT*ENTALPIA(TR,PR1);
    else x1=0.;
    *DH=x2-x1;
}

