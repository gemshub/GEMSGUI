// interpolation of tabulated values using Lagrange method
float TFGLcalc::LagranInterp(float *a, float *x, float *y, float x1, float x2,
                       int p,int p0,int p1)
{
    float s,z, s1[21];
    int i=0,j,j1,k,ja,ja1;

    if (x2 < a[0])
    { /*z=x2;*/ goto m3;
    }
    if(x1 < x[0])
    { /*z=x1;*/ goto m3;
    }
    //s=0.;
    for(j1=0;j1<p1;j1++)
        if (x2 >= a[j1] && x2 <= a[j1+1])
            goto m1;
    /*z=x2;*/
    goto m2;
m1:
    for(i=0;i<p0;i++)
        if(x1 >= x[i] && x1 <= x[i+1])
            goto m;
    //z=x1;
    if(x1 <= x[p0-1])
        goto m;
m2:
    if(x2 <= a[p1-1])
        goto m;
m3: /* Error arg beyond the table size */
    return(1);
m:
    if(i < p0-p)
        j=i;
    else j=p0-p-1;
    if(j1 >= p1-p)
        j1=p1-p-1;
    ja1=j1;
    for(ja=0;ja <= p; ja++)
    {
        s=0.;
        for(i=0;i<=p;i++)
        {
            z=1; //z1=1;
            for(k=0;k<=p;k++)
                if(k!=i)
                    z*=(x1-x[k+j])/(x[i+j]-x[k+j]);
            s+=y[i+j+(j1)*p0]*z;
        }
        s1[ja]=s;
        j1++;
    }
    s=0.;
    for(i=0;i<=p;i++)
    {
        z=1; //z1=1;
        for(k=0;k<=p;k++)
            if(k!=i)
                z*=(x2-a[k+ja1])/(a[i+ja1]-a[k+ja1]);
        s+=s1[i]*z;
    }
    return(s);
}

